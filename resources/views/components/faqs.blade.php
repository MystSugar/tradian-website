<script>
    function showFAQSection(section) {
        var content = document.querySelector(`#${section}Div .text`);
        var plusIcon = document.querySelector(`#${section}Div .plus-icon`);
        var minusIcon = document.querySelector(`#${section}Div .minus-icon`);

        var allSections = document.querySelectorAll('.text');
        allSections.forEach(function(sec) {
            if (sec !== content) {
                sec.classList.add('hidden', 'transition', 'ease-in-out', 'duration-300');
            }
        });

        if (content.classList.contains('hidden', 'transition', 'ease-in-out', 'duration-300')) {
            content.classList.remove('hidden', 'transition', 'ease-in-out', 'duration-300');
            plusIcon.classList.add('hidden', 'transition', 'ease-in-out', 'duration-300');
            minusIcon.classList.remove('hidden', 'transition', 'ease-in-out', 'duration-300');
        } else {
            content.classList.add('hidden', 'transition', 'ease-in-out', 'duration-300');
            plusIcon.classList.remove('hidden', 'transition', 'ease-in-out', 'duration-300');
            minusIcon.classList.add('hidden', 'transition', 'ease-in-out', 'duration-300');
        }
    }
    
</script>